class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    sort(a.begin() , a.end());
    int s = 0;
    int e = m-1;
    int mim = INT_MAX;
    while(e<n){
       if(a[e]-a[s] < mim){
           mim = a[e]-a[s];
       }
       s++;
       e++;
        
    }
    return mim;
    
    
    }   
};

