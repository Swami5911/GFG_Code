class Solution {
  public:
    int nthFibonacci(int n){
        // code here
        long long a=1,b=1,c=0;
        if(n==1||n==2) return 1;
        while(n-2){
            c=(a+b)%1000000007;
            a=b;
            b=c;
            n--;
            
        }
        return c;
    }
};

