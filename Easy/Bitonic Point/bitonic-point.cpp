class Solution{
public:
	
	int findMaximum(int arr[], int n) {
	    // code here
	    int i=0;
        int j=n-1;
        
        while(i<=j){
            if(arr[i] < arr[j]){
                i++;
            }else{
                j--;
            }
        }
        
        return max(arr[i],arr[j]);

	}
};

